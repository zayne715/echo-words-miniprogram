function buildSystemPrompt(styleKey) {
  const base = STORY_SYSTEM_PROMPT;
  const addon =
    styleKey && STORY_STYLE_ADDONS[styleKey] ? `\n\n--- 本风格补充 ---\n${STORY_STYLE_ADDONS[styleKey]}` : "";
  return base + addon;
}

function buildUserContent(words, genreKey) {
  const genreLabel = GENRE_LABEL[genreKey] || genreKey || "";
  return JSON.stringify(
    {
      words: Array.isArray(words) ? words : [],
      genreKey,
      genreLabel,
    },
    null,
    2
  );
}

